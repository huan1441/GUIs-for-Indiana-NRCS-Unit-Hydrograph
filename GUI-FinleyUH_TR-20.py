# A function to create the customized dimensionless unit hydrograph points for TR-20 input files

def DUH_TR20(PRF,delta_t_Tp,inpFile):
    """Please enter the specific value of Peak Rate Factor(PRF)
       and the nondimensional time interval of DUH."""

    # estimate the Gamma equation shape factor (m) based on Bhuya (2003)
    
    PRF = float(PRF)
    phi = PRF/645.33
    delta_t_Tp = float(delta_t_Tp)

    if phi >= 0.35:
        m = 6.29*phi**1.998+0.157
    else:
        m = 5.53*phi**1.75+0.04

    # create a list to store the coordinates (q/Qp) of DUH

    q_Qp = []

    for t_Tp in np.arange(0,100,delta_t_Tp):
        temp = np.exp((1-t_Tp)*m)*(t_Tp**m)
        q_Qp.append(temp)

        if t_Tp>1 and temp<0.00005:
            break

    # output the coordinates in the specific format of TR-20
    # 5 numbers in each row and 10 placeholders for each data point

    DUH = ["\n",
       "DIMENSIONLESS UNIT HYDROGRAPH:\n"]

    for i in range(len(q_Qp)):

        if i%5 == 0:
            temp = " "*20
            DUH.append(temp)

        temp = "%-10.4f"%q_Qp[i]
        DUH.append(temp)

        if (i+1)%5 == 0:
            temp = "\n"
            DUH.append(temp)

    DUH.append("\n")

    f = open(str(inpFile)+".inp", "a")

    f.writelines(DUH)

    f.close()

    return DUH


